- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.devices.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"timelineCell";
    
    timelineCell *cell = (timelineCell *)[tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    if (cell == nil)
    {
        NSArray *nib = [[NSBundle mainBundle] loadNibNamed:@"timelineCell" owner:self options:nil];
        cell = [nib objectAtIndex:0];
    }
    
    // Configure the cell...
    NSManagedObject *device = [self.devices objectAtIndex:indexPath.row];
    [cell.labelName setText:[NSString stringWithFormat:@"%@ %@", [device valueForKey:@"username"], [device valueForKey:@"name"]]];
    //[cell.imageTimeline setImage :[UIImage imageNamed:[device valueForKey:@"image"]]];
    //[cell.detailTextLabel setText:[device valueForKey:@"phone"]];
    
    UIImage *image = [UIImage imageWithData:[device valueForKey:@"image"]];
    
    [cell.imageTimeline setImage:image];
    return cell;
}

